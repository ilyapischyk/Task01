package com.pischyk.task1.exception;

public class ArrayLenghtException extends Exception {
    public ArrayLenghtException() {
        super();
    }

    public ArrayLenghtException(String message) {
        super(message);
    }

    public ArrayLenghtException(String message, Throwable cause) {
        super(message, cause);
    }

    public ArrayLenghtException(Throwable cause) {
        super(cause);
    }

}
